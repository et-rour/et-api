export const generateUniqueName = () => {
    let randomName = Math.round(Math.random() * (names.length - 1));
    let randomSurname = Math.round(Math.random() * (surnames.length - 1));
    return {
        name: names[randomName],
        surname: surnames[randomSurname],
        fullName: `${names[randomName]} ${surnames[randomSurname]}`,
    }
};

export const pickPhone = () => {
    let phoneNumber = 0;
    while (phoneNumber.toString().length !== 10) {
        let aux = Math.round(Math.random() * 10000000000);
        phoneNumber = aux;
    }
    return phoneNumber;
};

export const generateUniqueEmail = () => {
    let randomPrefix = emailPrefix[Math.round(Math.random() * (emailPrefix.length - 1))];
    let randomPostfix = emailPostfix[Math.round(Math.random() * (emailPostfix.length - 1))];
    return `${randomPrefix}@${randomPostfix}`;
};

